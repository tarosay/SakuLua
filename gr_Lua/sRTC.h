//	day: 日
//	hour: 時
//	min: 分
//	sec: 秒
//**************************************************
int adkRTCsetDateTime( lua_State *LuaLinkP );

//**************************************************
// RTCの時計を取得する: RTC.get
//	RTC.get()
//
// 西暦, 月, 日, 時, 分, 秒
//**************************************************
int adkRTCgetDateTime( lua_State *LuaLinkP );
